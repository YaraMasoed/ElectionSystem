    private void postbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_postbtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) ElectionTable.getModel();
        int SelectedRow = ElectionTable.getSelectedRow();
        Election e = a.viewElections().get(SelectedRow);
        a.setElections(e);
        if (a.postResults()) {
            JOptionPane.showMessageDialog(this, "Results posted");
        } else {
            JOptionPane.showMessageDialog(this, "No results to be posted");
        }

    }//GEN-LAST:event_postbtnActionPerformed

