    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed

        c.setName(tfname.getText());
        c.setCandidateID(Integer.parseInt(tfcandidateid.getText()));
        c.setYearsOfexp(Integer.parseInt(tfyearsofexp.getText()));
        c.setEmail(tfemail.getText());
        c.setDateOfBirth(tfdob.getText());
        c.setUsername(tfusername.getText());
        c.setPassword(tfPass.getText());

        String posistion = (String) Posistion.getSelectedItem();
        char gender = ' ';

        if (rbMale.isSelected()) {
            gender = 'M';
        } else if (rbFemale.isSelected()) {
            gender = 'F';
        } else {
            // Handle case where no gender is selected (optional)
            JOptionPane.showMessageDialog(this, "Please select a gender");
            return;
        }

        c.updateProfile();
        JOptionPane.showMessageDialog(this, "Profile Updated");
    }//GEN-LAST:event_updatebtnActionPerformed

