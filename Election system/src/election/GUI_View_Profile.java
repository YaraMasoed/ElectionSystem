    private void fillDetails() {
        tfname.setText(c.getName());
        if (c.getGender() == 'F') {
            rbFemale.setSelected(true);
        } else {
            rbMale.setSelected(true);
        }
        tfcandidateid.setText(String.valueOf(c.getCandidateID()));
        tfcandidateid.setEditable(false);
        tfyearsofexp.setText(String.valueOf(c.getYearsOfexp()));
        tfemail.setText(c.getEmail());
        if (c.getPosition() == "President") {
            Posistion.setSelectedIndex(0);
        } else {
            Posistion.setSelectedIndex(1);
        }
        tfdob.setText(c.getDateOfBirth());
        tfusername.setText(c.getUsername());
        tfPass.setText(c.getPassword());
    }

